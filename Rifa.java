package PrimerParcial;

public class Rifa {
    private int numero;
    private String descripcion;

    public Rifa(int numero, String descripcion) {
        this.numero = numero;
        this.descripcion = descripcion;
    }

    public int getNumero() {
        return numero;
    }

    public String getDescripcion() {
        return descripcion;
    }
}

